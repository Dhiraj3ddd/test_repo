package one ;

public class SquarePattern
{
    public static void main (String args[])
    {
        {
        System.out.println (" Printing with FOR loop "); 
        for(int line = 1;
                line<= 4;
                line ++  ) 
            {
            System.out.println (" # # # # ") ;       
            }
        }
    
        {
        int l =1;
        System.out.println (" Printing with WHILE loop ") ;    

        while (l <= 4 )
           {
           

            System.out.println (" *  *  *  * ") ;
            l++;
           }
        }
    }
}

    

       
